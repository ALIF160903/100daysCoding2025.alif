package day5;
public class Day5 {
    public static void main(String[] args) {
        byte a = 10;       // 8-bit
        short b = 200;     // 16-bit
        int c = 1000;      // 32-bit
        long d = 100000L;  // 64-bit

        System.out.println("byte  : " + a);
        System.out.println("short : " + b);
        System.out.println("int   : " + c);
        System.out.println("long  : " + d);
    }
    
}
